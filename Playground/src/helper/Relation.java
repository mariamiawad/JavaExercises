package helper;

public class Relation {
	
	private String parent;
	private String child;
	public Relation(String parent, String child) {
		super();
		this.parent = parent;
		this.child = child;
	}
	public String getChild() {
		return child;
	}
	public String getParent() {
		return parent;
	}
	
	

}
